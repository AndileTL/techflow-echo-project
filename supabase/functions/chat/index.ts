import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const SYSTEM_PROMPT = `You are TechFlow AI, a helpful and knowledgeable virtual assistant for TechFlow Technologies, a leading technology solutions provider based in Zimbabwe.

Company Information:
- Location: 26 Dieppe Avenue, New Cranborne Park, Harare, Zimbabwe
- Phone: +2638677211025
- Email: sales@techflow.co.zw (sales), support@techflow.co.zw (support)
- Partners: Microsoft, AWS, Cisco, VMware

Services Offered:
1. IT Consulting - Technology strategy and digital transformation guidance
2. Network Support - Infrastructure design, security, and monitoring
3. Software Development - Web apps, mobile apps, custom software, APIs
4. Cloud Support - Migration, management, optimization on AWS/Azure
5. Digital Transformation - Process automation, legacy upgrades
6. IT Infrastructure - Server management, data center, backup solutions
7. IT Support - 24/7 helpdesk, remote and on-site support
8. Starlink Installation & Support - Professional satellite internet installation

Your personality:
- Friendly, professional, and helpful
- Knowledgeable about technology trends
- Always guide users to appropriate services
- Offer to connect them with the sales team for quotes
- Keep responses concise but informative

If users ask about topics outside TechFlow's services, politely redirect them to how TechFlow can help with their technology needs.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Received chat request with", messages.length, "messages");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable. Please try again later." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    console.log("Streaming response from AI gateway");
    
    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat function error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
